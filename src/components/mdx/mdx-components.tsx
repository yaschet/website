/**
 * Inline Code
 */
export function Code({
  className,
  ...props
}: ComponentPropsWithoutRef<"code">) {
  // Check if inside a <pre> (code block) - don't add inline styling
  const isInPre = className?.includes("language-");

  if (isInPre) {
    return <code className={className} {...props} />;
  }

  return (
    <code
      className={cn(
        "relative rounded-none px-[0.3rem] py-[0.15rem] font-mono text-[0.85em] font-medium",
        // Anti-gray: High contrast border + clear background
        "border border-surface-200 dark:border-surface-700",
        "bg-surface-50 dark:bg-surface-900",
        "text-surface-900 dark:text-surface-50",
        // Hover effect for interactivity
        "transition-colors duration-200 hover:border-surface-300 dark:hover:border-surface-600",
        className,
      )}
      {...props}
    />
  );
}

