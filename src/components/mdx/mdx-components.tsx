/**
 * Img - Image component.
 */
export function Img({ className, alt, src, ...props }: ComponentPropsWithoutRef<"img">) {
	if (!src) return null;
	const resolvedSrc = resolveAsset(src as string);
	const isStatic = typeof resolvedSrc !== "string";

	return (
		<figure className="mb-8">
			<div className="relative aspect-video w-full border border-surface-200 dark:border-surface-800">
				<Image
					src={resolvedSrc}
					alt={alt || ""}
					fill
					className={cn("object-cover", className)}
					placeholder={isStatic ? "blur" : "empty"}
					{...(props as Omit<React.ComponentProps<typeof Image>, "src" | "alt">)}
				/>
			</div>
			{alt && (
				<figcaption className="mt-3 text-center font-mono text-muted-foreground text-xs">
					{alt}
				</figcaption>
			)}
		</figure>
	);
}

