/**
 * Inline Code
 */
export function Code({ className, ...props }: ComponentPropsWithoutRef<"code">) {
	// Check if inside a <pre> (code block) - don't add inline styling
	const isInPre = className?.includes("language-");

	if (isInPre) {
		return <code className={className} {...props} />;
	}

	return (
		<code
			className={cn(
				"relative rounded-none px-[0.3rem] py-[0.15rem] font-medium font-mono text-[0.85em]",
				"border border-surface-200 dark:border-surface-700",
				"bg-surface-50 dark:bg-surface-900",
				"text-surface-900 dark:text-surface-50",
				"transition-colors duration-200 hover:border-surface-300 dark:hover:border-surface-600",
				className,
			)}
			{...props}
		/>
	);
}

import { isValidElement, type ReactElement } from "react";
import { MermaidDiagram } from "@/src/components/mdx/mermaid-diagram";

/**
 * Code Block (Pre).
 */
export function Pre({ className, children, ...props }: ComponentPropsWithoutRef<"pre">) {
	// Check if this is a mermaid block
	if (isValidElement(children)) {
		const child = children as ReactElement<{ children?: string; className?: string }>;
		if (child.props?.className?.includes("language-mermaid")) {
			return <MermaidDiagram code={child.props.children || ""} />;
		}
	}

	// Extract the raw code text from the children (which is typically a <code> element)
	const rawCode = isValidElement(children)
		? (children as ReactElement<{ children?: React.ReactNode }>).props.children
		: children;

	return (
		<CodeBlockWrapper className={className} {...props}>
			{rawCode}
		</CodeBlockWrapper>
	);
}

