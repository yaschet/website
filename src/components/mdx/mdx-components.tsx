/**
 * Anchor - Links
 */
export function A({ className, ...props }: ComponentPropsWithoutRef<"a">) {
  // Check if this is an anchor link generated by rehype-autolink-headings
  const isAnchor = className?.includes("anchor");

  if (isAnchor) {
    return (
      <a
        className={cn(
          "ml-2 inline-block opacity-0 transition-opacity group-hover:opacity-100",
          "text-muted-foreground hover:text-foreground no-underline",
          className,
        )}
        {...props}
      />
    );
  }

  return (
    <a
      className={cn(
        "font-medium underline underline-offset-4",
        "text-primary hover:text-primary-600",
        "transition-colors",
        className,
      )}
      {...props}
    />
  );
}

